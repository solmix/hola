package org.solmix.hola.common.model;

import java.net.InetSocketAddress;
import java.util.HashMap;
import java.util.Map;

import org.solmix.commons.util.NetUtils;
import org.solmix.exchange.model.EndpointInfo;
import org.solmix.exchange.model.ServiceInfo;

/**
 * 描述信息，包含所有配置信息
 * 
 * @author solmix.f@gmail.com
 * @version $Id$ 2015年9月14日
 */

public class RemoteServiceInfo extends EndpointInfo
{

    protected String username;

    protected String password;

    protected String host;

    protected int port;

    protected String path;

    private volatile transient String ip;

    public RemoteServiceInfo()
    {
    }

    public RemoteServiceInfo(String protocol, String username, String password, String host, int port, String path)
    {
        if ((username == null || username.length() == 0) && password != null && password.length() > 0) {
            throw new IllegalArgumentException("Invalid url, password without username!");
        }
        setProtocol(new RemoteProtocolInfo(protocol));
        this.username = username;
        this.password = password;
        this.host = host;
        this.port = (port < 0 ? 0 : port);
        this.path = path;
        while (path != null && path.startsWith("/")) {
            path = path.substring(1);
        }
    }

    /**
     * @param serviceInfo
     * @param transporter
     */
    public RemoteServiceInfo(ServiceInfo serviceInfo, String transporter)
    {
        super(serviceInfo, transporter);
    }

    public InetSocketAddress toInetSocketAddress() {
        return new InetSocketAddress(getHost(), getPort());
    }
    
  
    public String getIp() {
        if (ip == null) {
            ip = NetUtils.getIpByHost(host);
        }
        return ip;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public String getHost() {
        return host;
    }

    public int getPort() {
        return port;
    }

    public String getPath() {
        return path;
    }

    public static RemoteServiceInfo valueOf(String url) {
        if (url == null || (url = url.trim()).length() == 0) {
            throw new IllegalArgumentException("url == null");
        }
        String protocol = null;
        String username = null;
        String password = null;
        String host = null;
        int port = 0;
        String path = null;
        Map<String, String> parameters = null;
        int i = url.indexOf("?");
        if (i >= 0) {
            String[] parts = url.substring(i + 1).split("\\&");
            parameters = new HashMap<String, String>();
            for (String part : parts) {
                part = part.trim();
                if (part.length() > 0) {
                    int j = part.indexOf('=');
                    if (j >= 0) {
                        parameters.put(part.substring(0, j), part.substring(j + 1));
                    } else {
                        parameters.put(part, part);
                    }
                }
            }
            url = url.substring(0, i);
        }
        i = url.indexOf("://");
        if (i >= 0) {
            if (i == 0)
                throw new IllegalStateException("url missing protocol: \"" + url + "\"");
            protocol = url.substring(0, i);
            url = url.substring(i + 3);
        } else {
            // case: file:/path/to/file.txt
            i = url.indexOf(":/");
            if (i >= 0) {
                if (i == 0)
                    throw new IllegalStateException("url missing protocol: \"" + url + "\"");
                protocol = url.substring(0, i);
                url = url.substring(i + 1);
            }
        }

        i = url.indexOf("/");
        if (i >= 0) {
            path = url.substring(i + 1);
            url = url.substring(0, i);
        }
        i = url.indexOf("@");
        if (i >= 0) {
            username = url.substring(0, i);
            int j = username.indexOf(":");
            if (j >= 0) {
                password = username.substring(j + 1);
                username = username.substring(0, j);
            }
            url = url.substring(i + 1);
        }
        i = url.indexOf(":");
        if (i >= 0 && i < url.length() - 1) {
            port = Integer.parseInt(url.substring(i + 1));
            url = url.substring(0, i);
        }
        if (url.length() > 0)
            host = url;

        RemoteServiceInfo ri = new RemoteServiceInfo(protocol, username, password, host, port, path);
        ri.setProperties(parameters);
        return ri;
    }

}
